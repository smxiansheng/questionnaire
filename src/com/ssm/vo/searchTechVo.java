public class searchTechVo {
	private String province;
	private String year;
	private String bigType;
	private String type;
	private String techType;
	private String techAssess;
	private String needAccess;
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getBigType() {
		return bigType;
	}
	public void setBigType(String bigType) {
		this.bigType = bigType;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTechType() {
		return techType;
	}
	public void setTechType(String techType) {
		this.techType = techType;
	}
	public String getTechAssess() {
		return techAssess;
	}
	public void setTechAssess(String techAssess) {
		this.techAssess = techAssess;
	}
	public String getNeedAccess() {
		return needAccess;
	}
	public void setNeedAccess(String needAccess) {
		this.needAccess = needAccess;
	}
	

}
